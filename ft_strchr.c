char	*ft_strchr(const char *s, int c)
{
	char	c2;
	int		i;

	i = 0;
	c2 = c;
	while (s[i] != 0 && s[i] != c2)
		i++;
	if (s[i] == c2)
		return ((char*)&s[i]);
	else
		return (0);
}
